package com.project.dto;

public class BandInviteDTO {
	private int inviteusercode;
	private int bandcode;
	private String userid;
	private String username;
	private String bandname;
	public int getInviteusercode() {
		return inviteusercode;
	}
	public void setInviteusercode(int inviteusercode) {
		this.inviteusercode = inviteusercode;
	}
	public int getBandcode() {
		return bandcode;
	}
	public void setBandcode(int bandcode) {
		this.bandcode = bandcode;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getBandname() {
		return bandname;
	}
	public void setBandname(String bandname) {
		this.bandname = bandname;
	}
	
}
